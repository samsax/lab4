    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        selectedIndex = jComboBox1.getSelectedIndex();
        try{
        
        if (selectedIndex == 0) {
            SalariedEmployee se = (SalariedEmployee) context.getBean("salariedEmployee");
            se.setFirstName(firstName.getText());
            se.setLastName(lastName.getText());
            se.setSsn(snn.getText());
            double salary = Double.parseDouble(jTextField1.getText());
            se.setSalary(salary);
            employees.add(se);
        } else if (selectedIndex == 1) {
            CommissionEmployee ce = (CommissionEmployee) context.getBean(CommissionEmployee.class);
            ce.setFirstName(firstName.getText());
            ce.setLastName(lastName.getText());
            ce.setSsn(snn.getText());
            double sale = Double.parseDouble(jTextField1.getText());
            ce.setGrossSales(sale);
            double commission = Double.parseDouble(jTextField2.getText());
            ce.setCommissionRate(commission);
            employees.add(ce);
        } else {
            HourlyEmployee he = (HourlyEmployee) context.getBean(HourlyEmployee.class);
            he.setFirstName(firstName.getText());
            he.setLastName(lastName.getText());
            he.setSsn(snn.getText());
            double wage = Double.parseDouble(jTextField1.getText());
            double hour = Double.parseDouble(jTextField2.getText());
            he.setHours(hour);
            he.setWage(wage);
            employees.add(he);
        }
        }
        catch (Exception e)
                {
                    String menssage = e.getMessage();
                    JOptionPane.showMessageDialog(null,menssage);
                }
        tabla = actualizar(employees);
        jTable1.setModel(tabla);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            employees.remove(selectedRow);
            tabla = actualizar(employees);
            jTable1.setModel(tabla);
        }
    }//GEN-LAST:event_deleteActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            Employee a = (Employee)employees.get(selectedRow);
            employees.remove(selectedRow);
            tabla = actualizar(employees);
            jTable1.setModel(tabla);
            firstName.setText(a.getFirstName());
            lastName.setText(a.getLastName());
            snn.setText(a.getSsn());
            if (a instanceof SalariedEmployee) {
                  SalariedEmployee b = (SalariedEmployee) a;
                  jComboBox1.setSelectedIndex(0);
                  jTextField1.setText(Double.toString(b.getSalary()));
                }
            else if(a instanceof HourlyEmployee){
                HourlyEmployee b = (HourlyEmployee) a;
                  jComboBox1.setSelectedIndex(1);
                  jTextField1.setText(Double.toString(b.getWage()));
                  jTextField2.setText(Double.toString(b.getHours()));
            }
            else {
                CommissionEmployee b = (CommissionEmployee) a;
                  jComboBox1.setSelectedIndex(1);
                  jTextField1.setText(Double.toString(b.getGrossSales()));
                  jTextField2.setText(Double.toString(b.getCommissionRate()));
            }
        }
        

    }//GEN-LAST:event_editActionPerformed

