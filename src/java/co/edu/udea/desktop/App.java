    public DefaultTableModel actualizar(List employees) {
        Vector columnNames = new Vector();
        columnNames.add("First name");
        columnNames.add("Last name");
        columnNames.add("SNN");
        columnNames.add("Salary");
        DefaultTableModel actual = new DefaultTableModel(columnNames, 0);
        for (Iterator it = employees.iterator(); it.hasNext();) {

            Employee e = (Employee) it.next();
            Vector employee = new Vector();
            employee.add(e.getFirstName());
            employee.add(e.getLastName());
            employee.add(e.getSsn());
            employee.add(e.payment());
            actual.addRow(employee);
        }
        return actual;
    }

